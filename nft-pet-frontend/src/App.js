function App() {
  const [petName, setPetName] = useState("");
  const [petDetails, setPetDetails] = useState(null);
  const [provider, setProvider] = useState(null);
  const [contract, setContract] = useState(null);

  const connectWallet = async () => {
    if (window.ethereum) {
      const newProvider = new ethers.providers.Web3Provider(window.ethereum);
      await newProvider.send("eth_requestAccounts", []);
      const signer = newProvider.getSigner();
      const newContract = new ethers.Contract(
        contractAddress,
        contractABI,
        signer
      );
      setProvider(newProvider);
      setContract(newContract);
    } else {
      console.error("MetaMask is not installed");
    }
  };

  const createPet = async () => {
    if (contract) {
      const tx = await contract.createPet(petName);
      await tx.wait();
      console.log("Pet created!");
    }
  };

  const getPetDetails = async () => {
    if (contract) {
      const details = await contract.getPetDetails(1); // Assuming ID 1
      setPetDetails(details);
    }
  };

  return (
    <div className="App">
      <h1>Digital Pet NFT</h1>
      <button onClick={connectWallet}>Connect Wallet</button>
      <div>
        <input
          type="text"
          placeholder="Enter pet name"
          value={petName}
          onChange={(e) => setPetName(e.target.value)}
        />
        <button onClick={createPet}>Create Pet</button>
      </div>
      <div>
        <button onClick={getPetDetails}>Get Pet Details</button>
        {petDetails && (
          <div>
            <p>Name: {petDetails.name}</p>
            <p>Level: {petDetails.level.toString()}</p>
            <p>Experience: {petDetails.experience.toString()}</p>
            <p>Hunger: {petDetails.hunger.toString()}</p>
          </div>
        )}
      </div>
    </div>
  );
}

export default App;
